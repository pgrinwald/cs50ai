from logic import *

AKnight = Symbol("A is a Knight")
AKnave = Symbol("A is a Knave")

BKnight = Symbol("B is a Knight")
BKnave = Symbol("B is a Knave")

CKnight = Symbol("C is a Knight")
CKnave = Symbol("C is a Knave")

## define a XOR function which at the end of the day, helps
## shorten many of the solutions.
def Xor(P, Q):
    return And(Or(P, Q), Not(And(P, Q)))

## These sentences apply to all of the puzzles.
default_rules = And(Xor(AKnight, AKnave), 
        Xor(BKnight, BKnave), 
        Xor(CKnight, CKnave))

# Puzzle 0
# A says "I am both a knight and a knave."
knowledge0 = And(
    default_rules,
    Implication(AKnight, And(AKnight, AKnave)), Implication(AKnave, Not(And(AKnight, AKnave)))
)

# Puzzle 1
# A says "We are both knaves."
# B says nothing.
knowledge1 = And(
    default_rules,
    Biconditional(AKnight,(And(AKnave, BKnave)))
)

# Puzzle 2
# A says "We are the same kind."
# B says "We are of different kinds."
knowledge2 = And(
    default_rules,
    Biconditional(AKnight, Or(And(AKnight, BKnight),And(AKnave, BKnave))),
    Biconditional(BKnight, Or(And(AKnight, BKnave),And(AKnave, BKnight)))

    #Implication(AKnight, And(AKnight, BKnight)),
    #Implication(AKnave, Not(And(AKnave, BKnave))),
    #Implication(BKnight, And(BKnight, AKnave)),
    #Implication(BKnave, Not(And(BKnave, AKnight)))        
)

# Puzzle 3
# A says either "I am a knight." or "I am a knave.", but you don't know which.
# B says "A said 'I am a knave'."
# B says "C is a knave."
# C says "A is a knight."
knowledge3 = And(
    default_rules,

    # If A is a knight, then B would have to be a Knave because it is impossible
    # for a character to assert that it is a Knave
    Implication(AKnight, BKnave),
    Implication(AKnave, BKnave),

    # If B is a knight then C is a Knave and vice versa
    Implication(BKnight, CKnave),
    Implication(BKnave, CKnight),

    # If C is a Knight then A is a Knight and vice versa
    Implication(CKnight, AKnight),
    Implication(CKnave, AKnave)
)


def main():
    symbols = [AKnight, AKnave, BKnight, BKnave, CKnight, CKnave]
    puzzles = [
        ("Puzzle 0", knowledge0),
        ("Puzzle 1", knowledge1),
        ("Puzzle 2", knowledge2),
        ("Puzzle 3", knowledge3)
    ]
    for puzzle, knowledge in puzzles:
        print(puzzle)
        if len(knowledge.conjuncts) == 0:
            print("    Not yet implemented.")
        else:
            for symbol in symbols:
                if model_check(knowledge, symbol):
                    print(f"    {symbol}")


if __name__ == "__main__":
    main()
